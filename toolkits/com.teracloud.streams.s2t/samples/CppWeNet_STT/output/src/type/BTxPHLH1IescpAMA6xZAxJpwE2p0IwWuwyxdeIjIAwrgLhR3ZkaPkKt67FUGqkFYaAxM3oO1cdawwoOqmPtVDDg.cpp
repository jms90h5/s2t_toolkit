TupleMappings* SELF::initMappings()
{
    instantiators_.insert(std::make_pair("tuple<rstring transcription,float64 confidence,uint64 audioTimestamp>",&initer));
    TupleMappings * tm = new TupleMappings();
#define MY_OFFSETOF(member, base) \
    ((uintptr_t)&reinterpret_cast<Self*>(base)->member) - (uintptr_t)base
   
    // initialize the mappings 
    
    {
        std::string s("transcription");
        TypeOffset t(MY_OFFSETOF(transcription_, tm), 
                     Meta::Type::typeOf<SPL::rstring >(), 
                     &typeid(SPL::rstring));
        addMapping(*tm, t, s, 0);
    }
    
    {
        std::string s("confidence");
        TypeOffset t(MY_OFFSETOF(confidence_, tm), 
                     Meta::Type::typeOf<SPL::float64 >(), 
                     &typeid(SPL::float64));
        addMapping(*tm, t, s, 1);
    }
    
    {
        std::string s("audioTimestamp");
        TypeOffset t(MY_OFFSETOF(audioTimestamp_, tm), 
                     Meta::Type::typeOf<SPL::uint64 >(), 
                     &typeid(SPL::uint64));
        addMapping(*tm, t, s, 2);
    }
    
    return tm;
}

